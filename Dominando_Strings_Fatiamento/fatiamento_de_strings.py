texto = "Este é um texto de teste, para fatiamento de strings"

print(texto[0])       # apenas é mostrado o elemento 0 
print(texto[:5])      # é mostrado as 5 primeiras posições
print(texto[10:])     # vai até o elemento 10
print(texto[10:16])   # vai definir o inicio e fim 
print(texto[10:16:2]) # com o inicio e fim vai trazer com diferença de 2 posições 
print(texto[:])       # é mostrada a string toda
print(texto[::-1])    # mostrado o espelo da string