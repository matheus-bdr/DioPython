lista = ['b','d','r','-','m','a','t','h','e','u','s']

print("1 - " , lista[2:])    #pulando a posição 0 e 1
print("2 - " , lista[:2])    #pegando apenas os dois primeiros elementos 
print("3 - " , lista[1:3])   #indo do elemento 1 a 3 
print("4 - " , lista[0:3:2]) #indo do elemento de 0 a 3 pulando 2
print("5 - " , lista[::])    #mostra td
print("6 - " , lista[::-1])  #mostra espelho